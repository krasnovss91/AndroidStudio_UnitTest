package com.example.myapplication311

class Math {
    fun sum(a: Int, b: Int): Int {
        return a + b
    }

    fun substract(a: Int, b: Int): Int {
        return a - b
    }

    fun mul(a: Int, b: Int): Int {
        return a * b
    }

    fun div(a: Int, b: Int): Int{
        return a / b
    }
}